package com.example.lenovo_pc;

public class FoodT {
    private String nameF;
    private int imageIdF;
    private String price;
    private  String DM;

    public FoodT() {
    }

    public String getNameF() {
        return nameF;
    }

    public void setNameF(String nameF) {
        this.nameF = nameF;
    }

    public int getImageIdF() {
        return imageIdF;
    }

    public void setImageIdF(int imageIdF) {
        this.imageIdF = imageIdF;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getDM() {
        return DM;
    }

    public void setDM(String DM) {
        this.DM = DM;
    }
}
